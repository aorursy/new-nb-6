def get_mat(rotation, shear, height_zoom, width_zoom, height_shift, width_shift):
    # returns 3x3 transformmatrix which transforms indicies
        
    # CONVERT DEGREES TO RADIANS
    rotation = math.pi * rotation / 180.
    shear = math.pi * shear / 180.
    
    # ROTATION MATRIX
    c1 = tf.math.cos(rotation)
    s1 = tf.math.sin(rotation)
    one = tf.constant([1],dtype='float32')
    zero = tf.constant([0],dtype='float32')
    rotation_matrix = tf.reshape( tf.concat([c1,s1,zero, -s1,c1,zero, zero,zero,one],axis=0),[3,3] )
        
    # SHEAR MATRIX
    c2 = tf.math.cos(shear)
    s2 = tf.math.sin(shear)
    shear_matrix = tf.reshape( tf.concat([one,s2,zero, zero,c2,zero, zero,zero,one],axis=0),[3,3] )    
    
    # ZOOM MATRIX
    zoom_matrix = tf.reshape( tf.concat([one/height_zoom,zero,zero, zero,one/width_zoom,zero, zero,zero,one],axis=0),[3,3] )
    
    # SHIFT MATRIX
    shift_matrix = tf.reshape( tf.concat([one,zero,height_shift, zero,one,width_shift, zero,zero,one],axis=0),[3,3] )
    
    return K.dot(K.dot(rotation_matrix, shear_matrix), K.dot(zoom_matrix, shift_matrix))
def transform(image,label):
    # input image - is one image of size [dim,dim,3] not a batch of [b,dim,dim,3]
    # output - image randomly rotated, sheared, zoomed, and shifted
    if PADDING == "ZERO":
        DIM = IMAGE_SIZE[0]+2
        image = tf.image.pad_to_bounding_box(image, 1, 1, DIM+2, DIM+2)
    else:
        DIM = IMAGE_SIZE[0]
    XDIM = DIM%2 #fix for size 331
    
    rot = 15. * tf.random.normal([1],dtype='float32')
    shr = 5. * tf.random.normal([1],dtype='float32') 
    h_zoom = 1.0 + tf.random.normal([1],dtype='float32')/10.
    w_zoom = 1.0 + tf.random.normal([1],dtype='float32')/10.
    h_shift = 16. * tf.random.normal([1],dtype='float32') 
    w_shift = 16. * tf.random.normal([1],dtype='float32') 
  
    # GET TRANSFORMATION MATRIX
    m = get_mat(rot,shr,h_zoom,w_zoom,h_shift,w_shift) 

    # LIST DESTINATION PIXEL INDICES
    x = tf.repeat( tf.range(DIM//2,-DIM//2,-1), DIM )
    y = tf.tile( tf.range(-DIM//2,DIM//2),[DIM] )
    z = tf.ones([DIM*DIM],dtype='int32')
    idx = tf.stack( [x,y,z] )
    
    # ROTATE DESTINATION PIXELS ONTO ORIGIN PIXELS
    idx2 = K.dot(m,tf.cast(idx,dtype='float32'))
    idx2 = K.cast(idx2,dtype='int32')
    
    if PADDING == "REFLECT":
        # Shift origin to corner
        idx2 = tf.math.add(-idx2, DIM//2)
        # Identify out-of-bounds positions
        bounds_mask = K.cast(tf.math.logical_or(tf.math.less(idx2, 0), tf.math.greater(idx2, DIM-1)), dtype="int32")
        # Compute mirrored positions
        mirror_idxs = tf.math.multiply(tf.math.subtract(DIM-1, tf.math.floormod(idx2, DIM-1)),
                                        bounds_mask)
        # Clear masked indices
        idx2 = tf.math.multiply(tf.math.subtract(1, bounds_mask), idx2)
        # Fill with mirrored indices
        idx2 = tf.math.add(idx2, mirror_idxs)
        idx3 = tf.stack( [idx2[0,], idx2[1,]] )
    else:
        idx2 = K.clip(idx2,-DIM//2+XDIM+1,DIM//2)             
        idx3 = tf.stack( [DIM//2-idx2[0,], DIM//2-1+idx2[1,]] )
        
    d = tf.gather_nd(image,tf.transpose(idx3))
    
    if PADDING == "ZERO":
        image = tf.reshape(d,[DIM,DIM,3])
        image = tf.image.crop_to_bounding_box(image, 1, 1, DIM-2, DIM-2)
    else:
        image = tf.reshape(d,[DIM,DIM,3])
        
    return image,label
